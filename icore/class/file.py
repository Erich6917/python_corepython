# 文件模式 操作
# r 以读方式打开
# rU 或 Ua 以读方式打开, 同时提供通用换行符支持 (PEP 278)
# w 以写方式打开 (必要时清空)
# a 以追加模式打开 (从 EOF 开始, 必要时创建新文件)
# r+ 以读写模式打开
# w+ 以读写模式打开 (参见 w )
# a+ 以读写模式打开 (参见 a )
# rb 以二进制读模式打开
# wb 以二进制写模式打开 (参见 w )
# ab 以二进制追加模式打开 (参见 a )
# rb+ 以二进制读写模式打开 (参见 r+ )
# wb+ 以二进制读写模式打开 (参见 w+ )
# ab+ 以二进制读写模式打开 (参见 a+ )


